int		ft_putchar(char c);

void	ft_print_alphabet(void)
{
	int		i;
	char	a;

	i = 0;
	a = 'a';
	while (i < 26)
	{
		ft_putchar(a);
		i = i + 1;
		a = a + 1;
	}
}
