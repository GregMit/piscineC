void	ft_putchar(char c);

int		main(int argc, char **argv)
{
	int		j;
	int		i;

	i = 0;
	j = 0;
	if (argc == 0)
		return (0);
	while (argv[j] != '\0')
		j++;
	j--;
	while (j > 0)
	{
		i = 0;
		while (argv[j][i] != '\0')
		{
			ft_putchar(argv[j][i]);
			i++;
		}
		ft_putchar('\n');
		j--;
	}
}
